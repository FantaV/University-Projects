// Consiste nell’utilizzare cicli come for, while o do-while per eseguire ripetutamente un blocco di codice fino a quando una certa condizione è soddisfatta.
// È il concetto di ripetere un’operazione usando cicli.

#include <stdio.h>

int main() {
    int i;
    for (i = 0; i < 5; i++) {
        printf("Iterazione numero %d\n", i);
    }
    return 0;
}